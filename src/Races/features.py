def raceFeatures(race,subRace):
    val = {
        "race": race,
        "subRace": subRace
    }
    features = _features[race]
    
    if subRace is None:
        val["features"] = features
        return val
    
    subFeatures = _features[subRace]
    keys = subFeatures.keys()
    for key in keys:
        features[key] = subFeatures[key]
    val["features"] = features
    return val

