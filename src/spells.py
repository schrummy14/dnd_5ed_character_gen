def getAllSpells():
    allSpells = list()
    for spell in _bardSpells:
        if not spell in allSpells:
            allSpells.append(spell)
    
    for spell in _clericSpells:
        if not spell in allSpells:
            allSpells.append(spell)
    
    for spell in _druidSpells:
        if not spell in allSpells:
            allSpells.append(spell)
    
    for spell in _paladinSpells:
        if not spell in allSpells:
            allSpells.append(spell)
    
    for spell in _rangerSpells:
        if not spell in allSpells:
            allSpells.append(spell)
    
    for spell in _sorcererSpells:
        if not spell in allSpells:
            allSpells.append(spell)
    
    for spell in _warlockSpells:
        if not spell in allSpells:
            allSpells.append(spell)
    
    for spell in _wizardSpells:
        if not spell in allSpells:
            allSpells.append(spell)
    
    return allSpells.sort()

def getSpell(sName):
    val = dict()
    val[sName] = _allSpells[sName]
    return val

def getRandomSpell(level = None, spellClass = None):
    '''
    Return a random spell in the supplied range and from the specified class
    '''
    if level is None:
        level = [-1, 99]
    avalSpells = _allSpells.keys()
    returnedSpell = dict()
    while True:
        spellName = sample(avalSpells,1)[0]
        randSpell = _allSpells[spellName]
        if level[0] < randSpell["level"] and randSpell["level"] < level[1]:
            if spellClass is None:
                returnedSpell[spellName] = randSpell
                break
    return returnedSpell


